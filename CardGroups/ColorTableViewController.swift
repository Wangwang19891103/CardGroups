import UIKit

class ColorTableViewController: UITableViewController {
    let data = ColorData()
    
    override func viewDidLoad() {
        super.viewDidLoad()
        self.tableView.separatorStyle = UITableViewCellSeparatorStyle.None
    }
    
    override func numberOfSectionsInTableView(tableView: UITableView) -> Int {
        return 1
    }
    
    override func tableView(tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        return data.colors.count
    }
    
    override func tableView(tableView: UITableView, cellForRowAtIndexPath indexPath: NSIndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCellWithIdentifier("Cell", forIndexPath: indexPath) as! ColorTableViewCell
        let entry = data.colors[indexPath.row]
        cell.layer.cornerRadius = 15
        cell.colorImg.backgroundColor = entry.color
        cell.nameLbl.text = entry.content
        cell.colorname.text = entry.colorname
        //let image = UIImage(named: entry.filename)
        //cell.bkImageView.image = image
        //cell.headingLabel.text = entry.heading
        return cell
    }
    
    
